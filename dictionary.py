dict1 = {'a': 10, 'b': 25}
print("Original Dictionary:", dict1)

# Add a key-value pair to the dictionary
dict1['c'] = 38
print("Dictionary after adding key 'c':", dict1)

# Remove a key-value pair from the dictionary
del dict1['a']
print("Dictionary after removing key 'a':", dict1)

# Modify a value in the dictionary
dict1['b'] = 20
print("Dictionary after modifying value of 'b':", dict1)