list1 = [1, 2, 3, 4]
print("Original List:", list1)

# Add an element to the list
list1.append(5)
print("List after adding 5:", list1)

# Remove an element from the list
list1.remove(2)
print("List after removing 2:", list1)

# Modify an element in the list
list1[1] = 9
print("List after modifying first element:", list1)